# Відсотки по депозитах
p = int(input())  # відсоток по депозитах в рік
x = int(input())  # баланс депозиту в грн
y = int(input())  # баланс по депозиту в коп
bal = x * 100 + y  # переводимо баланс у копійки
s = (bal + bal * p * 10**-2)    # сума на кінець року в копійках;
kop = int(s)    # відкидаємо від суми в копійках дрібну частину
hrn = int(kop / 100)    # переводимо в грн
print(hrn, kop % 100)   # друге знач-ня - дві ост. цифри числа - і є копійки
