n = int(input())
if n % 10 == 1 and n // 10 % 10 != 1:
    # перша умова - всі числа, які мають остачу 1; друга - числа, в яких десятки не =1 ( тобто 10-19)
    print(n, 'korova', sep=' ')
elif 5 > n % 10 > 1 != n // 10 % 10:
    # перша - числа. які при діленні на 10, мають остачу 1-4; друга - теж саме, що й зверху
    print(n, 'korovy', sep=' ')
else:
    print(n, 'korov', sep=' ')
