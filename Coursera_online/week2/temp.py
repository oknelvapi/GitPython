# По данному натуральном n вычислите сумму 1²+2²+3²+...+n²
n = int(input())
sumN = 0
i = 1   # число прогресії, з якого почнеться відлік
while i <= n:   # це означає, що цикл оббчислення закінчиться на заданому числі
    sumN = sumN+i**2    # кожен наступний квадрат числа додаватиметься до попереднього
    i += 1    # лічильник, збільшення числа на 1
print(sumN)
